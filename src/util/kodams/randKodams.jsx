function Kodams() {
    let random = parseInt(Math.random() * (10 - 1) + 1)

    switch (random) {
        case 1:
            return "gayung bolong"
        case 2:
            return "curut kejepit"
        case 3:
            return "anjing pit bull"
            
        case 4:
            return "ayam bertelur"
            
        case 5:
            return "siluman kodok"
            
        case 6:
            return "siluman lalat"
            
        case 7:
            return "jordi"
            
        case 8:
            return "jablay tua"
        case 9: 
            return "kura kura"
        case 10:
            return "harimau putih"
        
    }
}

export default Kodams